// Оголошення змінних різних типів
let myString: string = "Hello, TypeScript!";
let myNumber: number = 42;
let myBoolean: boolean = true;
let myAny: any = "I can be anything!";

// Виведення значень змінних у консоль
console.log("String:", myString);
console.log("Number:", myNumber);
console.log("Boolean:", myBoolean);
console.log("Any:", myAny);

// Оголошення масивів для рядків і чисел
let stringArray: string[] = ["apple", "banana", "cherry"];
let numberArray: number[] = [10, 20, 30];

// Виведення значень масивів у консоль
console.log("String Array:", stringArray);
console.log("Number Array:", numberArray);
